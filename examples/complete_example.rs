//! Complete HTML to PDF Conversion Example
//!
//! This example demonstrates the full HTML to PDF conversion pipeline:
//! 1. Parsing HTML from a string
//! 2. Parsing CSS from a string
//! 3. Building the layout tree
//! 4. Computing styles
//! 5. Performing layout
//! 6. Generating PDF output
//!
//! Run with: cargo run --example complete_example

use html2pdf::{
    css, html, layout, pdf,
    types::{Color, Margins, Orientation, PaperSize, Rect},
    Config, Input,
};
use std::io::Cursor;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    println!("=== HTML2PDF Complete Example ===\n");

    // Step 1: Parse HTML from a string
    println!("Step 1: Parsing HTML...");
    let html_content = create_sample_html();
    let document = html::parse_html(&html_content)?;
    println!("  ✓ Parsed HTML document: {:?}", &document.title);

    // Step 2: Parse CSS from a string
    println!("\nStep 2: Parsing CSS...");
    let css_content = create_sample_css();
    let stylesheet = css::parse_stylesheet(&css_content)?;
    println!("  ✓ Parsed stylesheet with {} rules", stylesheet.rules.len());

    // Step 3: Create layout context with custom settings
    println!("\nStep 3: Setting up layout context...");
    let layout_ctx = layout::LayoutContext::with_page_size(PaperSize::A4, Orientation::Portrait)
        .with_margins(Margins::new(72.0, 72.0, 72.0, 72.0)); // 1 inch margins
    println!("  ✓ Page size: A4 Portrait");
    println!("  ✓ Margins: 72pt (1 inch) on all sides");

    // Step 4: Build and perform layout
    println!("\nStep 4: Building layout tree...");
    let mut engine = layout::LayoutEngine::with_context(layout_ctx.clone());
    engine.add_stylesheet(stylesheet);
    
    let layout_tree = engine.layout_document(&document)?;
    println!("  ✓ Layout tree built successfully");

    // Step 5: Collect positioned boxes
    println!("\nStep 5: Collecting positioned boxes...");
    let boxes = layout::collect_positioned_boxes(&layout_tree);
    println!("  ✓ Found {} positioned boxes", boxes.len());

    // Print some layout information
    println!("\n  Layout Tree Structure:");
    layout::print_layout_tree(&layout_tree, 1);

    // Step 6: Generate PDF
    println!("\nStep 6: Generating PDF...");
    let pdf_bytes = generate_pdf(&boxes, &layout_ctx)?;
    println!("  ✓ Generated PDF: {} bytes", pdf_bytes.len());

    // Step 7: Alternative: Use the high-level API
    println!("\nStep 7: Using high-level API...");
    let high_level_pdf = generate_with_high_level_api()?;
    println!("  ✓ Generated PDF using high-level API: {} bytes", high_level_pdf.len());

    // Write PDFs to files
    let output_path = "examples/output_complete_example.pdf";
    std::fs::write(output_path, &pdf_bytes)?;
    println!("\n  ✓ PDF written to: {}", output_path);

    let output_path2 = "examples/output_high_level_api.pdf";
    std::fs::write(output_path2, &high_level_pdf)?;
    println!("  ✓ PDF written to: {}", output_path2);

    println!("\n=== Example Complete! ===");
    println!("Open the PDF files to see the results.");

    Ok(())
}

