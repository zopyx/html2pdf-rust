/// Generate PDF from layout boxes
fn generate_pdf(
    boxes: &[&layout::LayoutBox],
    ctx: &layout::LayoutContext,
) -> Result<Vec<u8>, Box<dyn std::error::Error>> {
    // Create PDF writer
    let mut writer = pdf::PdfWriter::new();
    writer.set_paper_size(PaperSize::A4, Orientation::Portrait);
    writer.set_margins(ctx.margins);
    writer.init_document();
    writer.set_info(
        "HTML2PDF Complete Example",
        "HTML2PDF Example",
        "html2pdf-rs v0.1.0",
    );

    // Add fonts
    writer.add_standard_font("F1", "Helvetica");
    writer.add_standard_font("F2", "Helvetica-Bold");
    writer.add_standard_font("F3", "Times-Roman");
    writer.add_standard_font("F4", "Courier");

    // Create page content
    let page_height = ctx.page_height();
    let mut content = pdf::PageContent::new();

    // Set up initial text state
    content.save_state();
    content.set_fill_color(Color::new(51, 51, 51)); // #333

    // Render each box
    for box_ in boxes {
        render_box(box_, &mut content, page_height)?;
    }

    content.restore_state();

    // Add the page to the document
    writer.add_page(content);

    // Write to buffer
    let mut output = Cursor::new(Vec::new());
    writer.write(&mut output)?;

    Ok(output.into_inner())
}

/// Render a single layout box to PDF content
fn render_box(
    box_: &layout::LayoutBox,
    content: &mut pdf::PageContent,
    page_height: f32,
) -> Result<(), Box<dyn std::error::Error>> {
    use layout::BoxType;

    let dims = &box_.dimensions;
    let border_box = dims.border_box();

    // Convert y-coordinate to PDF system (origin at bottom-left)
    let pdf_y = page_height - border_box.y - border_box.height;

    match box_.box_type {
        BoxType::Block | BoxType::InlineBlock => {
            // Draw border if present
            if dims.border.top > 0.0
                || dims.border.right > 0.0
                || dims.border.bottom > 0.0
                || dims.border.left > 0.0
            {
                content.save_state();
                content.set_line_width(0.5);
                content.set_stroke_color(Color::new(200, 200, 200));
                content.draw_rect(Rect::new(border_box.x, pdf_y, border_box.width, border_box.height));
                content.stroke();
                content.restore_state();
            }
        }
        BoxType::TextRun => {
            if let Some(ref text) = box_.text_content {
                let trimmed = text.trim();
                if !trimmed.is_empty() {
                    content.save_state();
                    content.begin_text();
                    content.set_font("F1", 12.0);
                    content.set_fill_color(Color::new(51, 51, 51));
                    content.text_position(border_box.x, pdf_y + 10.0);
                    content.show_text(trimmed);
                    content.end_text();
                    content.restore_state();
                }
            }
        }
        BoxType::Inline => {
            // Inline containers don't render directly
        }
        BoxType::Anonymous => {
            // Anonymous boxes are for layout only
        }
        _ => {}
    }

    Ok(())
}

/// Generate PDF using the high-level convenience API
fn generate_with_high_level_api() -> Result<Vec<u8>, Box<dyn std::error::Error>> {
    // Create HTML content
    let html = r#"
        <h1>High-Level API Example</h1>
        <p>This PDF was generated using the simple high-level API.</p>
        <p>The API handles all the details: parsing, layout, and PDF generation.</p>
        <table>
            <tr><th>Step</th><th>Description</th></tr>
            <tr><td>1</td><td>Parse HTML</td></tr>
            <tr><td>2</td><td>Parse CSS</td></tr>
            <tr><td>3</td><td>Build Layout</td></tr>
            <tr><td>4</td><td>Generate PDF</td></tr>
        </table>
    "#;

    // Create input
    let input = Input::Html(html.to_string());

    // Create configuration with custom settings
    let config = Config {
        paper_size: PaperSize::A4,
        orientation: Orientation::Portrait,
        margins: Margins::all(72.0),
        page_width: None,
        page_height: None,
        header: None,
        footer: Some("<p>Page from high-level API</p>".to_string()),
        user_stylesheets: vec![create_sample_css()],
        base_url: None,
        timeout_seconds: 30,
        debug_layout: false,
    };

    // Convert to PDF
    let pdf_bytes = html2pdf::html_to_pdf_from_input(&input, &config)?;

    Ok(pdf_bytes)
}
